export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.ru;

export const getTranslation = (language: Language, key: TranslationKey, params?: Record<string, string | number>): string => {
  const translation = translations[language]?.[key] || translations.ru[key] || key;
  
  if (params) {
    return Object.entries(params).reduce(
      (text, [param, value]) => text.replace(new RegExp(`\\{${param}\\}`, 'g'), String(value)),
      translation
    );
  }
  
  return translation;
};

